        Map<String, String> startSemesters,
        Map<String, String> endSemesters,
      })> getGPASemesters({
    required String cookie,
    required ZhengFangUserProvider? zhengFangUserProvider,
  }) async {
    return withAutoLoginRetry(
      initialCookie: cookie,
      zhengFangUserProvider: zhengFangUserProvider,
      action: (activeCookie) async {
        final html = await fetchGPASemetersZF(cookie: activeCookie);
        return parseGPASemestersFromHtmlZF(html);
      },
    );
  }

  /// 从正方教务系统获取绩点排名
  Future<GpaResponseZF> getGPA({
    required String cookie,

    /// 起始学年学期，如 "202503", "202512"
    required String startSemester,

    /// 终止学年学期，如 "202503", "202512"
    required String endSemester,

    /// 课程属性. 全部: "", 必修: "bx", 选修: "xx"
    required String courseType,
    required ZhengFangUserProvider? zhengFangUserProvider,
  }) async {
    return withAutoLoginRetry(
      initialCookie: cookie,
      zhengFangUserProvider: zhengFangUserProvider,
      action: (activeCookie) async {
        final result = await fetchGPAZF(
          cookie: activeCookie,
          startSemester: startSemester,
          endSemester: endSemester,
          courseType: courseType,
        );
        return parseGPAZF(result);
      },
    );
